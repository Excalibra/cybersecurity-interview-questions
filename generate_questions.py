import os
import json
from pathlib import Path

def generate_categories_js():
    base_dir = Path("docs/questions")
    categories = {
        "web": {"title": "Web Application Penetration Testing", "items": []},
        "red": {"title": "Red Team Operations", "items": []},
        "seceng": {"title": "Security Engineer", "items": []}
    }

    for category_key, cat_data in categories.items():
        category_dir = base_dir / category_key
        if not category_dir.exists():
            continue

        # Get all .md files except index.md
        md_files = sorted([f for f in category_dir.glob("*.md") if f.name != "index.md"])
        
        for md_file in md_files:
            # Extract title from first line (# Title)
            try:
                with open(md_file, "r", encoding="utf-8") as f:
                    first_line = f.readline().strip()
                    if first_line.startswith("# "):
                        title = first_line[2:].strip()
                    else:
                        title = md_file.stem.replace("-", " ").title()
            except:
                title = md_file.stem.replace("-", " ").title()

            relative_link = f"/cybersecurity-interview-questions/questions/{category_key}/{md_file.name.replace('.md', '/')}"
            
            cat_data["items"].append({
                "text": title,
                "link": relative_link
            })

    # Generate JS file
    js_content = f"""// Auto-generated by generate_questions.py - DO NOT EDIT MANUALLY
const categories = {json.dumps(categories, indent=2)};
"""

    with open("docs/questions/categories.js", "w", encoding="utf-8") as f:
        f.write(js_content)
    
    print(f"✅ Generated {sum(len(cat['items']) for cat in categories.values())} questions.")

if __name__ == "__main__":
    generate_categories_js()
